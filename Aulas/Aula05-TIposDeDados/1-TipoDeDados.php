<?php

    /*  TIPOS DE DADOS

        As variáveis podem ser de diferentes tipos de dados
        A mesma variável pode assumir esses diferentes tipos
    */
    $variavel = 100;
    $variavel = "Cem";
    # Atribuição de tipos diferentes para a mesma variável.

    # Apesar das variáveis em PHP serem de tipo implícito
    # é importante conhecer os tipos de variáveis.
    $inteiro = 100;         # São valores inteiros
    $float = 10.5;          # São valores seguidos de casa decimal
    $bool = true;           # Assume valor falso ou verdadeiro
    $string = "Bom dia";    # É cadeia caracteres
    $array = [1, 2, 3];     # É uma coleção de valores
    $nulo = null;           # Variável com valor NULO
