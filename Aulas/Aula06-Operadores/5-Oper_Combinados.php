<?php
    /*  OPERADORES DE ATRIBUIÇÃO COMBINADOS

        Permitem que alteremos o valor de uma variável a partir do valor original

        Exemplos:
    */

    // Operadores comuns
    $x = 10;
    $x = $x + 10;   # 20
    $x = $x - 17;   # 13
    $x = $x * 2;    # 26
    $x = $x / 2;    # 13
    echo $x . "<br>";

    // Operadores combinados
    $x = 10;
    $x += 10;   # 20
    $x -= 17;   # 13
    $x *= 2;    # 26
    $x /= 2;    # 13
    echo $x . "<br>";

?>